#//////////////////////////////////////////////////////////////////////////////////
# Pointクラス
# -----------------------   インスタンス変数   -----------------------------------
#   @y  y座標を保持
#   @x  x座標を保持
# --------------------  インクルードするモジュール -------------------------------
#   　なし
# ---------------------------- 内容説明 ------------------------------------------
#   boardの座標を保持するクラス
#   石が置ける場所の情報のやりとりに使われる
#//////////////////////////////////////////////////////////////////////////////////
class Point
  def initialize(y, x)
    @y = y
    @x = x
  end

  attr_accessor :y, :x
end
